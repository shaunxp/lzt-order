package com.lzt.order.test.newfun.j8.stream;

public class Student {

    int no;
    String name;
    String sex;
    float height;

    public int getNo() {
        return no;
    }

    public String getName() {
        return name;
    }

    public String getSex() {
        return sex;
    }

    public float getHeight() {
        return height;
    }

    public Student(int no, String name, String sex, float height) {
        this.no = no;
        this.name = name;
        this.sex = sex;
        this.height = height;
    }


}
